reaper.Undo_BeginBlock()
local stagedefault = 1
stage = tonumber(reaper.GetExtState('DemCycleMIDIcclaneshow', 'sstage')) or stagedefault
if stage == 1 then
  hwnd = reaper.MIDIEditor_GetActive()
  reaper.MIDIEditor_OnCommand(hwnd, reaper.NamedCommandLookup("_BR_ME_SET_CC_LANES_HEIGHT_170")) 
  reaper.SetExtState('DemCycleMIDIcclaneshow', 'sstage', 2, false)
end
if stage == 2 then
  hwnd = reaper.MIDIEditor_GetActive()
  reaper.MIDIEditor_OnCommand(hwnd, reaper.NamedCommandLookup("_BR_ME_SET_CC_LANES_HEIGHT_0")) 
  reaper.SetExtState('DemCycleMIDIcclaneshow', 'sstage', 1, false)
end
reaper.Undo_EndBlock( "Cycle Show CC Lanes", -1)
