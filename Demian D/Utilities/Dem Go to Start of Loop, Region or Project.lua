local finalpos = 0
local editcur = reaper.GetCursorPosition()

local  loopstart, loopend = reaper.GetSet_LoopTimeRange( 0, 1, 0, 0, 0 )
local retval, regionidx = reaper.GetLastMarkerAndCurRegion( 0, editcur )
local _, _, rgnstart, _, _, _ = reaper.EnumProjectMarkers( regionidx )

function GoLoopStart()
  if loopstart ~= loopend then 
      finalpos = loopstart
  end
end

function GoRegionStart()
  if loopstart == loopend then
    finalpos = rgnstart
  end
end

GoLoopStart()
GoRegionStart()
reaper.SetEditCurPos( finalpos, 1, 1 )

--reaper.ShowConsoleMsg( rgnstart..",,"..editcur.."\n" )
reaper.UpdateArrange() -- Update the arrangement (often needed)
