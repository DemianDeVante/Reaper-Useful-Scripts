local threshold=10^-10
reaper.Undo_BeginBlock()
reaper.PreventUIRefresh( 1 )
pos=reaper.GetCursorPosition()
track_count = reaper.CountSelectedTracks( 0 )
for ntrack = 0, track_count -1 do
  track = reaper.GetSelectedTrack( 0, ntrack )
  item_count = reaper.CountTrackMediaItems( track )
  for nitem = 0, item_count -1 do
    item =  reaper.GetTrackMediaItem( track, nitem )
    item_pos=reaper.GetMediaItemInfo_Value( item, "D_POSITION")
    item_track=reaper.GetMediaItem_Track( item )
    item_length=reaper.GetMediaItemInfo_Value( item, "D_LENGTH")
    is_selected = reaper.IsMediaItemSelected( item )
    if item_pos <= pos+threshold and item_pos + item_length > pos+threshold then 
        is_selected = reaper.IsMediaItemSelected( item )
        if is_selected then
          undodescription = "Unselect Item"
          reaper.SetMediaItemSelected( item, 0)
        else
          undodescription = "Select Item"
          reaper.SetMediaItemSelected( item, 1)
        end
        isitemundercursor = 1
     end
  end
end
if not isitemundercursor then
  undodescription = "Clear Item Selection"
  reaper.Main_OnCommand( 40289, 0 )
end
reaper.PreventUIRefresh( -1 )
reaper.Undo_EndBlock( undodescription, 4 )
reaper.UpdateArrange()
