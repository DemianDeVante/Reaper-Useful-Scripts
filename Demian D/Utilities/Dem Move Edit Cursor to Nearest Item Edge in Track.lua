function NearestValue(table, number)
    local smallestSoFar, smallestIndex
    for i, y in ipairs(table) do
        if not smallestSoFar or (math.abs(number-y) < smallestSoFar) then
            smallestSoFar = math.abs(number-y)
            smallestIndex = i
        end
    end
    return smallestIndex, table[smallestIndex]
end


local track = reaper.GetSelectedTrack( 0, 0 )
if not track then return end
local valtable={}
local cur_pos = reaper.GetCursorPosition()
local item_cnt = reaper.CountTrackMediaItems( track )
for i = 0, item_cnt-1 do
  local item = reaper.GetTrackMediaItem( track, i )
  local item_pos = reaper.GetMediaItemInfo_Value( item, "D_POSITION" )
  local item_end = item_pos + reaper.GetMediaItemInfo_Value( item, "D_LENGTH" )
  table.insert(valtable,item_pos)
  table.insert(valtable,item_end)
end
_,closest_time = NearestValue(valtable, cur_pos)
reaper.SetEditCurPos( closest_time, true, false )
reaper.defer(function() end)
