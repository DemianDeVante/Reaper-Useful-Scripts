reaper.Undo_BeginBlock()
local stagedefault = 1
stage = tonumber(reaper.GetExtState('DemCycleAutomationMode', 'sstage')) or stagedefault
if stage == 1 then
  reaper.Main_OnCommand(40400,0)
  reaper.SetExtState('DemCycleAutomationMode', 'sstage', 2, false)
end
if stage == 2 then
  reaper.Main_OnCommand(40403,0)
  reaper.SetExtState('DemCycleAutomationMode', 'sstage', 3, false)
end
if stage == 3 then
  reaper.Main_OnCommand(40404,0)
  reaper.SetExtState('DemCycleAutomationMode', 'sstage', 4, false)
end
if stage == 4 then
  reaper.Main_OnCommand(40402,0)
  reaper.SetExtState('DemCycleAutomationMode', 'sstage', 1, false)
end
reaper.Undo_EndBlock( "Cycle Automation Mode", -1)
