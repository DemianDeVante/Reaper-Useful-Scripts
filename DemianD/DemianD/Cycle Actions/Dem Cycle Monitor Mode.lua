-- Function to set track monitoring mode
function setTrackMonitoringMode(track)
    local monitoringMode = reaper.GetMediaTrackInfo_Value(track, "I_RECMON")
    
    if monitoringMode ~= 0 then
        reaper.SetMediaTrackInfo_Value(track, "I_RECMON", 0)  -- Set monitoring mode to disabled
    else
        reaper.SetMediaTrackInfo_Value(track, "I_RECMON", 1)  -- Set monitoring mode to enabled
    end
end

-- Main function
function main()
    local selectedTracks = {}

    -- Get selected tracks
    for i = 0, reaper.CountSelectedTracks(0) - 1 do
        local track = reaper.GetSelectedTrack(0, i)
        table.insert(selectedTracks, track)
    end

    -- Set monitoring mode for each selected track
    for _, track in ipairs(selectedTracks) do
        setTrackMonitoringMode(track)
    end

    reaper.UpdateArrange()
end

-- Run the main function
reaper.Undo_BeginBlock()
main()
reaper.Undo_EndBlock("Set Monitoring Mode", -1)

